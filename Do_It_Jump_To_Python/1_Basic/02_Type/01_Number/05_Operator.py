# 사칙 연산
print('사칙연산')
a = 3
b = 4
print(a + b)
print(b - a)
print(a * b)
print(a / b)


'''
** 연산자
x ** y로 사용하면 x의 y제곱을 리턴.
'''
print('** 연산자')
print(a ** b)

'''
% 연산자
나누기의 나머지를 리턴.
'''
print('% 연산자')
print(7 % 3)
print(3 % 7)

'''
// 연산자
나눗셈 후 몫을 리턴.
'''
print('// 연산자')
print(7 / 4)
print(7 // 4)

print('-----------')
print(14 // 3)
print(14 % 3)